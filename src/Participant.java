/**
 * Participant of the competition.
 */
class Participant {
    String name;
    int score;

    /**
     * Initializes an instance of this class.
     *
     * @param name - Name of participant.
     * @param score - Participant's score.
     */
    Participant(String name, int score) {
        this.name = name;
        this.score = score;
    }
}
